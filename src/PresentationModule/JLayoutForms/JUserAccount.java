    private void LoadBoxes()
    {
        Campus campus = new Campus();
        Department department = new Department();

        staffCampus = campus.ReadCampus();
        staffDepartment = department.ReadDepartment();

        for (Campus campusData : staffCampus)
        {
            cmbStaffCampus.addItem(campusData.getCampusName());
        }

        for (Department departmentData : staffDepartment)
        {
            cmbStaffDepartment.addItem(departmentData.getDepartmentName());
        }
    }

