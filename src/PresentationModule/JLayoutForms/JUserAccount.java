    private void btnStaffUpdateMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnStaffUpdateMouseClicked
    {//GEN-HEADEREND:event_btnStaffUpdateMouseClicked
        boolean validInput = false;
        String name, surname, campus, department, cellNo, email, username, password, confirmPassword;

        name = txtStaffName.getText();
        surname = txtStaffSurname.getText();
        campus = cmbStaffCampus.getSelectedItem().toString();
        department = cmbStaffDepartment.getSelectedItem().toString();
        cellNo = txtStaffCellNo.getText();
        email = txtStaffEmail.getText();
        username = txtStaffUsername.getText();
        password = String.valueOf(txtStaffPassword.getPassword());
        confirmPassword = String.valueOf(txtStaffConfirmPassword.getPassword());

        try
        {
            Validation val = new Validation();
            validInput = val.ValidateStaffAccountInput(name, surname, campus, department, cellNo, email, username, password, confirmPassword);
            int campusID = 0;
            int deptID = 0;

            if (validInput)
            {
                for (Campus campusData : staffCampus)
                {
                    if (campusData.getCampusName().equals(campus))
                    {
                        campusID = campusData.getCampusID();
                    }
                }

                for (Department departmentData : staffDepartment)
                {
                    if (departmentData.getDepartmentName().equals(department))
                    {
                        deptID = departmentData.getDepartmentID();
                    }
                }
                if (campusID != 0 && deptID != 0)
                {
                    Staff staff = null;
                    if(password.length() == 0)
                    {
                        staff = new Staff(AuthenticationSettings.getConnectedStaff().getStaffID(), name, surname, campusID, deptID, cellNo, email, username, AuthenticationSettings.getConnectedStaff().getStaffPassword());
                    }
                    else
                    {
                        staff = new Staff(AuthenticationSettings.getConnectedStaff().getStaffID(), name, surname, campusID, deptID, cellNo, email, username, password);
                        staff.setStaffPassword(staff.getHashPassword());
                    }
                    staff.UpdateStaffEntry();
                    JOptionPane.showConfirmDialog(this, "Succesfully updated your account", "Update Succesful", JOptionPane.DEFAULT_OPTION);
                    AuthenticationSettings.setConnectedStaff(staff);

                    this.setVisible(false);
                    JStaffModule jsm = new JStaffModule();
                    jsm.setVisible(true);
                } else
                {
                    throw new Exception("Error in ID set for field!");
                }
            } else
            {
                ResetForm();
            }
        } catch (Exception ex)
        {
            Helper.DisplayError(ex.toString());
        }
    }//GEN-LAST:event_btnStaffUpdateMouseClicked

    private void ResetForm()
    {
        txtStaffName.setText(AuthenticationSettings.getConnectedStaff().getStaffName());
        txtStaffSurname.setText(AuthenticationSettings.getConnectedStaff().getStaffSurname());
        cmbStaffCampus.setSelectedIndex(staffCampus.indexOf(AuthenticationSettings.getConnectedStaff().getStaffID()));
        cmbStaffDepartment.setSelectedIndex(staffDepartment.indexOf(AuthenticationSettings.getConnectedStaff().getStaffDepartment()));
        txtStaffEmail.setText(AuthenticationSettings.getConnectedStaff().getStaffEmail());
        txtStaffCellNo.setText(AuthenticationSettings.getConnectedStaff().getStaffCellNo());

        txtStaffUsername.setText(AuthenticationSettings.getConnectedStaff().getStaffUsername());
        txtStaffPassword.setText("");
        txtStaffConfirmPassword.setText("");

        for (Campus campusData : staffCampus)
        {
            if (campusData.getCampusID() == AuthenticationSettings.getConnectedStaff().getStaffCampusID())
            {
                cmbStaffCampus.setSelectedItem(campusData.getCampusName());
            }
        }

        for (Department departmentData : staffDepartment)
        {
            if (departmentData.getDepartmentID() == AuthenticationSettings.getConnectedStaff().getStaffDepartment())
            {
                cmbStaffDepartment.setSelectedItem(departmentData.getDepartmentName());
            }
        }
    }

